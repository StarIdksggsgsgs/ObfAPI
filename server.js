import express from "express";
const app = express();
app.use(express.text({ type: "*/*" }));

function scoreMatches(input, patterns) {
  let score = 0;
  for (const p of patterns) if (p.test(input)) score++;
  return score;
}

app.post("/api", (req, res) => {
  const input = req.body || "";
  const json = (d, c) => res.json({ Obfuscator: d, Confidence: c });

  if (input.includes("https://lura.ph/")) return json("Luraph", "100%");
  if (/--\[\[\s*Ts file was generated by UPX/i.test(input)) return json("UPX", "100%");
  if (/--\s*this file was generated using ironbrew1/i.test(input)) return json("IB1", "100%");
  if (/--\s*this file was obfuscated with the unveilr obf/i.test(input)) return json("Threaded", "100%");
  if (/MoonVeil Obfuscator/i.test(input)) return json("MoonVeil", "100%");

  const alphabet = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz";
  for (let i = 0; i < alphabet.length; i++) {
    const v = alphabet[i];
    const r = new RegExp(`return\\s*\\(\\s*function\\s*\\(\\s*\\.\\.\\.\\s*\\)\\s*local\\s+${v}\\s*=\\s*\\{([\\s\\S]*?)\\}`);
    const m = input.match(r);
    if (m) {
      if (m[1].includes("=")) return json("Prometheus/25ms", "100%");
      return json("Prometheus", "100%");
    }
  }

  if (/--\[\[\s*v1\.0\.0\s*https:\/\/wearedevs\.net\/obfuscator\s*\]\]/.test(input)) {
    const wadPatterns = [
      /return\s*\(\s*function\(/,
      /local\s+[A-Za-z]\s*=\s*\{/,
      /for\s+[A-Za-z],[A-Za-z]\s+in\s+ipairs/
    ];
    const score = scoreMatches(input, wadPatterns);
    if (score > 0) return json("Wearedevs", "100%");
    return json("Unknown (Watermark found but patterns missing)", "50%");
  }

  const detectors = [
    {
      name: "Threaded",
      patterns: [
        /return\s*\(\s*function\([^)]*\)\s*return\s*\(\s*function/i,
        /while\s+[A-Za-z]\s+do/,
        /if\s+[A-Za-z]\s*<\s*\(/,
        /\(\d+\+1\)-1/,
        /\w+\[\(\d+\+1\)-1\]/,
        /local\s+[A-Za-z0-9_,]{40,}/
      ]
    },
    {
      name: "MoonVeil",
      patterns: [
        /Ee\(/,
        /Wc\[Ee\(/,
        /bit32\.bxor/,
        /string\.byte/,
        /string\.char/
      ]
    },
    {
      name: "Prometheus",
      patterns: [
        /return\s*\(\s*function\(/,
        /local\s+[A-Za-z]\s*=\s*\{/,
        /for\s+[A-Za-z],[A-Za-z]\s+in\s+ipairs/
      ]
    },
    {
      name: "25ms",
      patterns: [
        /[A-Za-z0-9+/]{6,}={0,2}/,
        /local\s+[A-Za-z]\s*=\s*\{/,
        /string\.char/
      ]
    },
    {
      name: "Luraph",
      patterns: [
        /bit32\.bxor/,
        /string\.gsub/,
        /repeat\s+if/
      ]
    },
    {
      name: "IronBrew",
      patterns: [
        /getfenv/,
        /setfenv/,
        /loadstring/
      ]
    }
  ];

  let best = { name: "Unknown", score: 0 };
  for (const d of detectors) {
    const s = scoreMatches(input, d.patterns);
    if (s > best.score) best = { name: d.name, score: s };
  }

  if (best.score > 0) {
    const percent = Math.min(99, best.score * 20);
    return json(
      best.name,
      `${percent}% (this obf is not supported this is just a calculation contact mruptimee. on discord to add support)`
    );
  }

  return json("Unknown", "CONTACT mruptimee TO ADD SUPPORT TO OBF");
});

app.listen(3000, () => console.log("Obf detector API running on port 3000"));
